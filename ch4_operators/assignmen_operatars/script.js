// Assignment Operators in JavaScript

var x = 10;     // Basic assignment (=)
console.log("x =", x);

// Add and assign (+=)
x += 5;         // same as x = x + 5
console.log("x += 5 →", x);  // 15

// Subtract and assign (-=)
x -= 3;         // same as x = x - 3
console.log("x -= 3 →", x);  // 12

// Multiply and assign (*=)
x *= 2;         // same as x = x * 2
console.log("x *= 2 →", x);  // 24

// Divide and assign (/=)
x /= 4;         // same as x = x / 4
console.log("x /= 4 →", x);  // 6

// Modulus and assign (%=)
x %= 5;         // same as x = x % 5
console.log("x %= 5 →", x);  // 1

// Exponentiation and assign (**=)
x **= 3;        // same as x = x ** 3 (power)
console.log("x **= 3 →", x); // 1
